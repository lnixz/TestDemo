function serachObjectForAttribute(arr, target, attribute, childrenName) {
  for (let i = 0; i < arr.length; i++) {
    if (arr[i][attribute] === target) {
      return [arr[i]];
    }
    if (Array.isArray(arr[i][childrenName])) {
      const targetArray = serachObjectForAttribute(arr[i][childrenName], target, attribute, childrenName);
      if (targetArray.length) {
        return [arr[i], ...targetArray]
      }
    }
  }
  return [];
}


